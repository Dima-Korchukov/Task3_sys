#!/bin/bash
if [ -z "$1" ]; then
  echo "Будьласка введіть шлях до директорії"
  exit 1
fi

echo "Видалення порожніх файлів у директорії: $1"
find "$1" -type f -empty -delete

echo "Видалення порожніх директорій у директорії: $1"
find "$1" -type d -empty -delete

echo "Очищення завершено для директорії: $1" 
